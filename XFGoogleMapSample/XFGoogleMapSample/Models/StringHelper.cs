using System;
using System.Collections.Generic;
using System.Text;

namespace XFGoogleMapSample.Models
{
    public static class StringHelper
    {

        public static string[] ArrUnicode { get; } =
        { "á", "à", "ả", "ã", "ạ", "â", "ấ", "ầ", "ẩ", "ẫ", "ậ", "ă", "ắ", "ằ", "ẳ", "ẵ", "ặ",
            "đ",
            "é","è","ẻ","ẽ","ẹ","ê","ế","ề","ể","ễ","ệ",
            "í","ì","ỉ","ĩ","ị",
            "ó","ò","ỏ","õ","ọ","ô","ố","ồ","ổ","ỗ","ộ","ơ","ớ","ờ","ở","ỡ","ợ",
            "ú","ù","ủ","ũ","ụ","ư","ứ","ừ","ử","ữ","ự",
            "ý","ỳ","ỷ","ỹ","ỵ",};

        public static string[] ArrAlphabet { get; } =
        { "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a", "a",
            "d",
            "e","e","e","e","e","e","e","e","e","e","e",
            "i","i","i","i","i",
            "o","o","o","o","o","o","o","o","o","o","o","o","o","o","o","o","o",
            "u","u","u","u","u","u","u","u","u","u","u",
            "y","y","y","y","y",};

        public static string RemoveUnicodeCharacter(this string text)
        {
            for (int i = 0; i < ArrUnicode.Length; i++)
            {
                text = text.Replace(ArrUnicode[i], ArrAlphabet[i]);
                text = text.Replace(ArrUnicode[i].ToUpper(), ArrAlphabet[i].ToUpper());
            }
            return text;
        }
    }
}
